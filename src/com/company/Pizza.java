package com.company;

import java.util.ArrayList;

public class Pizza {
  private int number;
  private String name;
  private String ingredients;
  private int price;

  public Pizza(int number, String name, String ingredients, int price) {
    this.number = number;
    this.name = name;
    this.ingredients = ingredients;
    this.price = price;
  }

  @Override
  public String toString() {
    return number + ". " + name;
  }
}
